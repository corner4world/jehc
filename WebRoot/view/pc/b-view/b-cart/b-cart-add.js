function addBCart(){
	reGetWidthAndHeight();
	initBCartFormAdd();
	bCartWinAdd = Ext.create('top.Ext.Window',{
		layout:'fit',                    
		width:clientWidth,                    
		height:clientHeight,
//		maximized:true, 
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,            
		plain:true,   
		modal:true,       
		title:'添加购物车信息',
		headerPosition:'top',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:bCartFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				if(getBCartDetailJSON() == false){
					return;
				}
				var params = {bCartDetailJSON:encodeURI(getBCartDetailJSON())}
				submitFormIncludeParams(bCartFormAdd,'../bCartController/addBCart',grid,bCartWinAdd,false,true,params);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bCartWinAdd.show();
	top.Ext.getCmp('b_cart_sessionid').setValue(JSESSIONID);
}
