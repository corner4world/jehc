var bCartOrderAddressWinAdd;
var bCartOrderAddressFormAdd;
function addBCartOrderAddress(){
	initBCartOrderAddressFormAdd();
	bCartOrderAddressWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				win.collapse();
			}
		},
		items:bCartOrderAddressFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(bCartOrderAddressFormAdd,'../bCartOrderAddressController/addBCartOrderAddress',grid,bCartOrderAddressWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bCartOrderAddressWinAdd.show();
}
