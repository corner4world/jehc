var bSellerExpressWinAdd;
var bSellerExpressFormAdd;
function addBSellerExpress(){
	initBSellerExpressFormAdd();
	bSellerExpressWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		headerPosition:'left',
		listeners:{
			minimize:function(win,opts){
				win.collapse();
			}
		},
		items:bSellerExpressFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(bSellerExpressFormAdd,'../bSellerExpressController/addBSellerExpress',grid,bSellerExpressWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	Ext.getCmp('b_seller_id_').setValue($('#b_seller_id').val());
	bSellerExpressWinAdd.show();
}
function initBSellerExpressFormAdd(){
	bSellerExpressFormAdd = Ext.create('Ext.FormPanel',{
		xtype:'form',
		waitMsgTarget:true,
		defaultType:'textfield',
		fieldDefaults:{
			labelWidth:70,
			labelAlign:'left',
			flex:1,
			margin:'2 5 4 5'
		},
		items:[
		{
			fieldLabel:'卖家编号',
			xtype:'textfield',
			hidden:true,
			name:'b_seller_id',
			id:'b_seller_id_',
			maxLength:32,
			anchor:'100%'
		},
		{
			fieldLabel:'公司名称',
			xtype:'textfield',
			name:'b_seller_express_name',
			maxLength:100,
			anchor:'100%'
		},
		{
			fieldLabel:'状&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;态',
			xtype:"combo",
            store:[["0","正常"],["1","关闭"]],
            emptyText:"请选择",
            mode:"local",
            value:'0',
            triggerAction:"all",
            editable:false,
			hiddenName:'b_seller_express_status',
			allowBlank:false,
			anchor:'25%',
			name:'b_seller_express_status'
		}
		]
	});
}
