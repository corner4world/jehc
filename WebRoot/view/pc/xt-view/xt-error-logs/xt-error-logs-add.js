var xtErrorLogsWinAdd;
var xtErrorLogsFormAdd;
function addXtErrorLogs(){
	initXtErrorLogsFormAdd();
	xtErrorLogsWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtErrorLogsFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtErrorLogsFormAdd,'../xtErrorLogsController/addXtErrorLogs',grid,xtErrorLogsWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtErrorLogsWinAdd.show();
}
function initXtErrorLogsFormAdd(){
	xtErrorLogsFormAdd = Ext.create('Ext.FormPanel',{
		xtype:'form',
		labelWidth:50,
		waitMsgTarget:true,
		defaultType:'textfield',
		autoScroll:true,
		fieldDefaults:{
			labelWidth:70,
			labelAlign:'left',
			flex:1,
			margin:'4 5 4 5'
		},
		items:[
		{
			fieldLabel:'异常日志内容',
			xtype:'textfield',
			name:'xt_error_logContent',
			maxLength:65535,
			anchor:'100%'
		},
		{
			fieldLabel:'异常日志级别',
			xtype:'numberfield',
			value:'0',
			name:'xt_error_logType',
			anchor:'100%'
		},
		{
			fieldLabel:'异常日志出错时间',
			xtype:'textfield',
			name:'xt_error_logTime',
			maxLength:20,
			anchor:'100%'
		},
		{
			fieldLabel:'操作人ID外键',
			xtype:'textfield',
			name:'xt_userinfo_id',
			maxLength:32,
			anchor:'100%'
		}
		]
	});
}
