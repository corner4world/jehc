var xtOperateLogsWinDetail;
var xtOperateLogsFormDetail;
function detailXtOperateLogs(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initXtOperateLogsFormDetail();
	xtOperateLogsWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtOperateLogsFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtOperateLogsWinDetail.show();
	loadFormData(xtOperateLogsFormDetail,'../xtOperateLogsController/getXtOperateLogsById?xt_operate_log_id='+ record.items[0].data.xt_operate_log_id);
}
