var xtQuartzLogWinAdd;
var xtQuartzLogFormAdd;
function addXtQuartzLog(){
	initXtQuartzLogFormAdd();
	xtQuartzLogWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtQuartzLogFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtQuartzLogFormAdd,'../xtQuartzLogController/addXtQuartzLog',grid,xtQuartzLogWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtQuartzLogWinAdd.show();
}
