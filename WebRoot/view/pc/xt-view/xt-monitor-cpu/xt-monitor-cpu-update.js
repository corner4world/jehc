var xtMonitorCpuWinEdit;
var xtMonitorCpuFormEdit;
function updateXtMonitorCpu(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initXtMonitorCpuFormEdit();
	xtMonitorCpuWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtMonitorCpuFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtMonitorCpuFormEdit,'../xtMonitorCpuController/updateXtMonitorCpu',grid,xtMonitorCpuWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtMonitorCpuWinEdit.show();
	loadFormData(xtMonitorCpuFormEdit,'../xtMonitorCpuController/getXtMonitorCpuById?xt_monitor_cpu_id='+ record.items[0].data.xt_monitor_cpu_id);
}
