var xtQuartzWinEdit;
var xtQuartzFormEdit;
function updateXtQuartz(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initXtQuartzFormEdit();
	reGetWidthAndHeight();
	xtQuartzWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:clientWidth*0.8,                    
		height:clientHeight, 
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtQuartzFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtQuartzFormEdit,'../xtQuartzController/updateXtQuartz',grid,xtQuartzWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtQuartzWinEdit.show();
	loadFormData(xtQuartzFormEdit,'../xtQuartzController/getXtQuartzById?id='+ record.items[0].data.id);
}
