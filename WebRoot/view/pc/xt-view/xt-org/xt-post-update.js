var xtPostWinEdit;
var xtPostFormEdit;
function updateXtPost(xt_departinfo_id,xt_post_parentId,xt_post_id){
	initXtPostFormEdit();
	xtPostWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑岗位信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtPostFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtPostFormEdit,'../xtPostController/updateXtPost',grid,xtPostWinEdit,false,true,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtPostWinEdit.show();
	new Ext.util.DelayedTask(function(){  
       Ext.getCmp('pdid').setValue(xt_departinfo_id);
       Ext.getCmp('pdtext').setValue(xt_departinfo_id);
       Ext.getCmp('ppid').setValue(xt_post_parentId);
       Ext.getCmp('pptext').setValue(xt_post_parentId);
    }).delay(200);
	loadFormData(xtPostFormEdit,'../xtPostController/getXtPostById?xt_post_id='+ xt_post_id);
}
