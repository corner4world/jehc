var xtKnowledgeWinAdd;
var xtKnowledgeFormAdd;
function addXtKnowledge(){
	initXtKnowledgeFormAdd();
	reGetWidthAndHeight();
	xtKnowledgeWinAdd = Ext.create('top.Ext.Window',{
		layout:'fit',
		width:clientWidth*0.8,                    
		height:clientHeight, 
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtKnowledgeFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtKnowledgeFormAdd,'../xtKnowledgeController/addXtKnowledge',grid,xtKnowledgeWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtKnowledgeWinAdd.show();
}
