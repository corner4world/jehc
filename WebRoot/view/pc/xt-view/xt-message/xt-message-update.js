var xtMessageWinEdit;
var xtMessageFormEdit;
function updateXtMessage(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initXtMessageFormEdit();
	xtMessageWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtMessageFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtMessageFormEdit,'../xtMessageController/updateXtMessage',grid,xtMessageWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtMessageWinEdit.show();
	
	loadFormData(xtMessageFormEdit,'../xtMessageController/getXtMessageById?xt_message_id='+ record.items[0].data.xt_message_id);
}
