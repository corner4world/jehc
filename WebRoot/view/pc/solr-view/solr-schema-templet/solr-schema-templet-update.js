var solrSchemaTempletWinEdit;
var solrSchemaTempletFormEdit;
function updateSolrSchemaTemplet(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initSolrSchemaTempletFormEdit();
	solrSchemaTempletWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximized:true,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:solrSchemaTempletFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(solrSchemaTempletFormEdit,'../solrSchemaTempletController/updateSolrSchemaTemplet',grid,solrSchemaTempletWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	solrSchemaTempletWinEdit.show();
	loadFormData(solrSchemaTempletFormEdit,'../solrSchemaTempletController/getSolrSchemaTempletById?solr_schema_templet_id='+ record.items[0].data.solr_schema_templet_id);
}
