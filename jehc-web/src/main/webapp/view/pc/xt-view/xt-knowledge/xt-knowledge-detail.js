function detailXtKnowledge(){
	reGetWidthAndHeight();
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initXtKnowledgeFormDetail();
	xtKnowledgeWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:clientWidth*0.8,                    
		height:clientHeight, 
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtKnowledgeFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtKnowledgeWinDetail.show();
	loadFormData(xtKnowledgeFormDetail,'../xtKnowledgeController/getXtKnowledgeById?xt_knowledge_id='+ record.items[0].data.xt_knowledge_id);
}
