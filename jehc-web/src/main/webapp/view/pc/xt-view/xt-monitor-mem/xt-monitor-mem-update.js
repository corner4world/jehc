var xtMonitorMemWinEdit;
var xtMonitorMemFormEdit;
function updateXtMonitorMem(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initXtMonitorMemFormEdit();
	xtMonitorMemWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtMonitorMemFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtMonitorMemFormEdit,'../xtMonitorMemController/updateXtMonitorMem',grid,xtMonitorMemWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtMonitorMemWinEdit.show();
	loadFormData(xtMonitorMemFormEdit,'../xtMonitorMemController/getXtMonitorMemById?xt_monitor_mem_id='+ record.items[0].data.xt_monitor_mem_id);
}
