var xtNoticeWinDetail;
var xtNoticeFormDetail;
function detailXtNotice(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initXtNoticeFormDetail();
	xtNoticeWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtNoticeFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtNoticeWinDetail.show();
	/**初始化附件右键菜单开始 参数4为1表示不拥有上传和删除功能 即明细页面使用**/
	initFileRight('xt_attachment_id','xt_attachment_id_pic',1,2);
	initFileRight('xt_attachment_id_','xt_attachment_id__pic',1,2);
	/**初始化附件右键菜单结束**/

	/**配置附件回显方法开始**/
	var params = {xt_attachment_id:record.items[0].data.xt_attachment_id+','+record.items[0].data.xt_attachment_id_,field_name:'xt_attachment_id,xt_attachment_id_'};
	ajaxFilePathBackRequest('../xtCommonController/getAttachmentPathPP',params,1);
	/**配置附件回显方法结束**/
	loadFormData(xtNoticeFormDetail,'../xtNoticeController/getXtNoticeById?xt_notice_id='+ record.items[0].data.xt_notice_id);
}
