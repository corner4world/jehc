var xtDataDictionaryWinAdd;
var xtDataDictionaryFormAdd;
function addXtDataDictionary(xt_data_dictionary_id){
	initXtDataDictionaryFormAdd();
	if(null != xt_data_dictionary_id && '' != xt_data_dictionary_id){
		Ext.getCmp('xt_data_dictionary_pid').setValue(xt_data_dictionary_id);
	}
	xtDataDictionaryWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtDataDictionaryFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtDataDictionaryFormAdd,'../xtDataDictionaryController/addXtDataDictionary',grid,xtDataDictionaryWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtDataDictionaryWinAdd.show();
}
