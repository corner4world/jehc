var xtMonitorCpuWinAdd;
var xtMonitorCpuFormAdd;
function addXtMonitorCpu(){
	initXtMonitorCpuFormAdd();
	xtMonitorCpuWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtMonitorCpuFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtMonitorCpuFormAdd,'../xtMonitorCpuController/addXtMonitorCpu',grid,xtMonitorCpuWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtMonitorCpuWinAdd.show();
}
