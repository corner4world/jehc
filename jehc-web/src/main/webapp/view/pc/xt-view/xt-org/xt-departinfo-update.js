var xtDepartinfoWinEdit;
var xtDepartinfoFormEdit;
function updateXtDepartinfo(xt_departinfo_id,xt_departinfo_parentId,text){
	initXtDepartinfoFormEdit();
	xtDepartinfoWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		title:'编辑部门信息',
		items:xtDepartinfoFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtDepartinfoFormEdit,'../xtDepartinfoController/updateXtDepartinfo',grid,xtDepartinfoWinEdit,false,true,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtDepartinfoWinEdit.show();
	Ext.getCmp('pid').setValue(xt_departinfo_id);
	new Ext.util.DelayedTask(function(){ 
	   /**
       Ext.getCmp('ptext').setValue(Ext.getCmp('pid').getRawValue());
       **/
       Ext.getCmp('ptext').setValue(text);
    }).delay(200);
	loadFormData(xtDepartinfoFormEdit,'../xtDepartinfoController/getXtDepartinfoById?xt_departinfo_id='+ xt_departinfo_id);
}
