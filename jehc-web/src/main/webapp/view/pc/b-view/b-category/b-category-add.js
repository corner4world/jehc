var bCategoryWinAdd;
var bCategoryFormAdd;
function addBCategory(b_category_id){
	initBCategoryFormAdd();
	bCategoryWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				win.collapse();
			}
		},
		items:bCategoryFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(bCategoryFormAdd,'../bCategoryController/addBCategory',grid,bCategoryWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bCategoryWinAdd.show();
	Ext.getCmp('b_category_pid').setValue(b_category_id);
}
