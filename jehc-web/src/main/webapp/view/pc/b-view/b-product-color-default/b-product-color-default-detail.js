var bProductColorDefaultWinDetail;
var bProductColorDefaultFormDetail;
function detailBProductColorDefault(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initBProductColorDefaultFormDetail();
	bProductColorDefaultWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximized:true,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				win.collapse();
			}
		},
		items:bProductColorDefaultFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bProductColorDefaultWinDetail.show();
	loadFormData(bProductColorDefaultFormDetail,'../bProductColorDefaultController/getBProductColorDefaultById?b_product_color_default_id='+ record.items[0].data.b_product_color_default_id);
}
