var bProductImgDefaultWinDetail;
var bProductImgDefaultFormDetail;
function detailBProductImgDefault(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要查看明细的项！');
		return;
	}
	initBProductImgDefaultFormDetail();
	bProductImgDefaultWinDetail = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximized:true,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'明细信息',
		listeners:{
			minimize:function(win,opts){
				win.collapse();
			}
		},
		items:bProductImgDefaultFormDetail,
		buttons:[{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	bProductImgDefaultWinDetail.show();
	Ext.getCmp('b_product_id').setValue($('#b_product_id_').val());
	Ext.getCmp('pic').getEl().dom.src = record.items[0].data.jehcimg_base_url+record.items[0].data.xt_attachmentPath;
	loadFormData(bProductImgDefaultFormDetail,'../bProductImgDefaultController/getBProductImgDefaultById?b_product_img_default_id='+ record.items[0].data.b_product_img_default_id);
}
